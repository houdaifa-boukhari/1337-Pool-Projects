void	ft_sort_int_tab(int *tab, int size)
{
	int	*p1;
	int	*p2;
	int	x;

	p1 = tab;
	p2 = tab + 1;
	while (p1 < tab + size)
	{
		while (p2 < tab + size)
		{
			if (*p2 < *p1)
			{
				x = *p1;
				*p1 = *p2;
				*p2 = x;
			}
			p2++;
		}
		p1++;
		p2 = p1 +1;
	}
}
